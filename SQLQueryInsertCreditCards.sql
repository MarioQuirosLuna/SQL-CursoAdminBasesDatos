USE IF5100_2022_B76090

DECLARE @intFlag INT = 1
DECLARE @local_MAX_VALUE BIGINT = 9999999999999999
DECLARE @local_MIN_VALUE BIGINT = 1111111111111111


WHILE (@intFlag <= 1000)
BEGIN

INSERT INTO CUSTOMERS.tb_CREDIT_CARD
(
	CREDIT_CARD_NUMBER
	,CREDIT_CARD_BRAND
	,EXPIRATION_MONTH
	,EXPIRATION_YEAR
	,CVV
	,IS_ACTIVE
	,IS_DELETED
	,LAST_MODIFIED_BY
	,LAST_MODIFIED_DATE
)
SELECT
 CAST(CAST(RAND()*(@local_MAX_VALUE-@local_MIN_VALUE)+@local_MIN_VALUE AS BIGINT) AS VARCHAR(16))
 ,CAST(RAND()*(4-1)+1 AS INT)
 ,CAST(RAND()*(13-1)+1 AS INT)
 ,CAST(RAND()*(2028-2022)+2021 AS INT)
 ,CAST(RAND()*(999-100)+100 AS INT)
 ,1
 ,0
 ,SUSER_NAME()
 ,GETDATE()

SET @intFlag = @intFlag + 1

END
GO

SELECT * FROM CUSTOMERS.tb_CREDIT_CARD